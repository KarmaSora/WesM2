<?php


function sum($num1, $num2){
   $sum = $num1 + $num2;
   return $sum;
}

function multiply($num1, $num2){
    $prod = $num1 * $num2;
    return $prod;
}
function divide($num1, $num2){
    $div = $num1 / $num2;
    return $div;
}
function subtr($num1, $num2){
    $sub = $num1 - $num2;
    return $sub;
}

?>